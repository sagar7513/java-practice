public class IfElsePractice{
	public static void main(String[] args){
		int code = 25;
		if (code<20){
			System.out.println("less than 20");
		}
		else{
			System.out.println("more than 20");
		}
		if(code%2==0){
			System.out.println("it is even number");
		}
		else{
			System.out.println("it is odd number");
		}
	}
}